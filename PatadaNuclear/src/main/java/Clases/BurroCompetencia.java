package Clases;


public class BurroCompetencia {
    
    private int burro;
    private int competencia;

    public BurroCompetencia(int burro, int competencia) {
        this.burro = burro;
        this.competencia = competencia;
    }

    public int getBurro() {
        return burro;
    }

    public void setBurro(int burro) {
        this.burro = burro;
    }

    public int getCompetencia() {
        return competencia;
    }

    public void setCompetencia(int competencia) {
        this.competencia = competencia;
    }
    
    
}
